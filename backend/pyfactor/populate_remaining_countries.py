def update_remaining_countries(batch_size=10, batch_number=1):
    """Update countries that don't have filing information yet."""
    
    # Get list of countries WITHOUT filing info
    countries_without_filing = list(GlobalSalesTaxRate.objects.filter(
        is_current=True,
        tax_authority_name__isnull=True
    ).values_list('country', flat=True).distinct().order_by('country'))
    
    logger.info(f"\nTotal countries without filing info: {len(countries_without_filing)}")
    
    # Process only the countries we have data for
    countries_to_process = [c for c in REMAINING_COUNTRY_DATA.keys() if c in countries_without_filing]
    
    if not countries_to_process:
        logger.info("No countries to process in this batch")
        return 0, 0
    
    logger.info(f"\n{'='*60}")
    logger.info(f"Processing Batch {batch_number}: {', '.join(countries_to_process)}")
    logger.info(f"{'='*60}")
    
    success_count = 0
    error_count = 0
    
    for country_code in countries_to_process:
        filing_info = REMAINING_COUNTRY_DATA[country_code]
        
        # Get the record for this country
        try:
            tax_rate = GlobalSalesTaxRate.objects.get(
                country=country_code,
                is_current=True
            )
            
            with transaction.atomic():
                # Basic filing info
                tax_rate.tax_authority_name = filing_info['tax_authority_name']
                tax_rate.filing_frequency = filing_info['filing_frequency']
                tax_rate.filing_day_of_month = filing_info.get('filing_day_of_month')
                tax_rate.online_filing_available = filing_info['online_filing_available']
                tax_rate.online_portal_name = filing_info['online_portal_name']
                tax_rate.online_portal_url = filing_info['online_portal_url']
                tax_rate.main_form_name = filing_info['main_form_name']
                tax_rate.filing_instructions = filing_info['filing_instructions']
                tax_rate.manual_filing_fee = Decimal(str(filing_info['manual_filing_fee']))
                tax_rate.online_filing_fee = Decimal(str(filing_info['online_filing_fee']))
                
                # Deadline fields
                tax_rate.filing_deadline_days = filing_info.get('filing_deadline_days')
                tax_rate.filing_deadline_description = filing_info.get('filing_deadline_description', '')
                tax_rate.grace_period_days = filing_info.get('grace_period_days', 0)
                tax_rate.penalty_rate = Decimal(str(filing_info.get('penalty_rate', 0))) if filing_info.get('penalty_rate') else None
                tax_rate.deadline_notes = filing_info.get('deadline_notes', '')
                
                tax_rate.save()
                success_count += 1
                logger.info(f"✅ {tax_rate.country_name} ({country_code}) - Updated successfully")
                
        except GlobalSalesTaxRate.DoesNotExist:
            error_count += 1
            logger.error(f"❌ {country_code} - Not found in database")
        except Exception as e:
            error_count += 1
            logger.error(f"❌ {country_code} - Error: {e}")
    
    # Show summary
    logger.info(f"\n{'='*60}")
    logger.info(f"Batch {batch_number} Complete")
    logger.info(f"Successfully updated: {success_count} countries")
    logger.info(f"Errors: {error_count}")
    logger.info(f"{'='*60}")
    
    # Check total progress
    total_with_filing = GlobalSalesTaxRate.objects.filter(
        is_current=True,
        tax_authority_name__isnull=False
    ).count()
    
    remaining = GlobalSalesTaxRate.objects.filter(
        is_current=True,
        tax_authority_name__isnull=True
    ).count()
    
    logger.info(f"\nOverall Progress:")
    logger.info(f"Countries with filing info: {total_with_filing}/300")
    logger.info(f"Countries remaining: {remaining}")
    
    # Save progress
    progress_data = {
        'timestamp': datetime.now().isoformat(),
        'batch_number': batch_number,
        'countries_processed': countries_to_process,
        'success_count': success_count,
        'error_count': error_count,
        'total_with_filing': total_with_filing,
        'remaining': remaining
    }
    
    filename = f'filing_info_progress.json'
    with open(filename, 'w') as f:
        json.dump(progress_data, f, indent=2)
    
    return success_count, error_count

